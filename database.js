// Helper function to search database
window.lookupIngredient = function(rawName) {
  const cleanName = rawName.trim().toLowerCase().replace(/[^a-z0-9\s-]/g, "");
  
  // Try exact match in E-Number registry (if user input was directly an E-Number)
  const ePattern = /^(e\s*\d{3,4}[a-z]?)$/i;
  if (ePattern.test(cleanName)) {
    const formattedE = cleanName.toUpperCase().replace(/\s+/g, "");
    if (eNumbersRegistry[formattedE]) {
      return {
        e_number: formattedE,
        hebrew_name: eNumbersRegistry[formattedE].hebrew_name,
        role: eNumbersRegistry[formattedE].role,
        details: eNumbersRegistry[formattedE].details
      };
    }
  }

  // 1. Check chemical/additive map (including partial matching sorted by length desc)
  const sortedChemicals = Object.keys(chemicalToENumberMap).sort((a, b) => b.length - a.length);
  for (const chem of sortedChemicals) {
    if (cleanName.includes(chem)) {
      const eNum = chemicalToENumberMap[chem];
      const data = eNumbersRegistry[eNum];
      return {
        e_number: eNum,
        hebrew_name: `${data.role} (${eNum})`, // e.g. מווסת חומציות (E330)
        role: data.role,
        details: data.details
      };
    }
  }

  // 2. Check standard ingredient map (including partial matching with word boundaries)
  const sortedIngredients = Object.keys(standardIngredientsMap).sort((a, b) => b.length - a.length);
  for (const ingName of sortedIngredients) {
    const regex = new RegExp(`\\b${ingName}\\b`, 'i');
    if (regex.test(cleanName)) {
      return {
        e_number: null,
        hebrew_name: standardIngredientsMap[ingName],
        role: null,
        details: "רכיב מזון סטנדרטי."
      };
    }
  }

  return null;
}
